#include <cstdio>
#include <cstring>
#include <algorithm>
using namespace std;
const int maxn = 1000010;

int n;
int a[maxn], b[maxn];

int main(){
    while(~scanf("%d", &n)){
        for(int i = 0; i < n; ++ i)
            scanf("%d", a + i);
        for(int i = 0; i < n; ++ i)
            scanf("%d", b + i);
        
    }
    return 0;
}
